const dialog=document.getElementById('kitchenDialog');let kitchenSteps=[],stepIndex=0,kitchenName='',kitchenLessonId='lesson-1';
function startKitchen(which){
  const data=which==='lesson1'?lesson1:lesson2;
  kitchenLessonId=which==='lesson1'?'lesson-1':'lesson-2';kitchenName=data.title;kitchenSteps=data.steps;stepIndex=0;renderKitchen();dialog.showModal();
}
function renderKitchen(){
  const step=kitchenSteps[stepIndex],state=lessonState[kitchenLessonId];
  document.getElementById('kitchenTitle').textContent=kitchenName;
  document.getElementById('kitchenTime').textContent=step[0];
  document.getElementById('kitchenStepTitle').textContent=step[1];
  document.getElementById('kitchenBody').innerHTML=`<p>${transformMeasurements(step[2],state.servings,state.units)}</p>`;
  document.getElementById('stepCounter').textContent=`Step ${stepIndex+1} of ${kitchenSteps.length}`;
  document.getElementById('progressBar').style.width=`${((stepIndex+1)/kitchenSteps.length)*100}%`;
  document.getElementById('prevStep').disabled=stepIndex===0;
  document.getElementById('nextStep').textContent=stepIndex===kitchenSteps.length-1?'Finish':'Next';
}
document.getElementById('prevStep').addEventListener('click',()=>{if(stepIndex>0){stepIndex--;renderKitchen()}});
document.getElementById('nextStep').addEventListener('click',()=>{if(stepIndex<kitchenSteps.length-1){stepIndex++;renderKitchen()}else dialog.close()});
document.getElementById('closeKitchen').addEventListener('click',()=>dialog.close());

